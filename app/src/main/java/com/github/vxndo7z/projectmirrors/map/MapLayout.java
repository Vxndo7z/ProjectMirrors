	public static int[][] readJsonMap(InputStream input) throws Exception {
		StringBuffer jsonString = new StringBuffer();
		BufferedReader br = new BufferedReader(new InputStreamReader(input));
		String line;
		while ((line = br.readLine()) != null) {
			jsonString.append(line + "\n");
		} JSONObject json = new JSONObject(jsonString.toString());
		int maps = json.getInt("maps");
		int[][] map = new int[maps][1];
		for (int i = 0; i < maps; i++) {
			map[i] = Arrays.copyOf(map[i], map[i].length+1);
			map[i] = getMapArray(json.getString("map"+i));
		} return map;
	}

	public static void writeJsonMap(OutputStream output, int[][] map) throws Exception {
		JSONObject json = new JSONObject();
		json.put("maps", map.length);
		for (int i = 0; i < map.length; i++) {
			for (int j = 0; j < map[i].length; j++) {
				json.put("map"+i, getMapString(map[i]));
			}
		} BufferedWriter br = new BufferedWriter(new OutputStreamWriter(output));
		br.write(json.toString(4));
		br.close();
	}

